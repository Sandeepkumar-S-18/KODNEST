package com.JavaBasics.Operators;

public class UnaryOperator {
	public static void main(String[] args) {
		
		// ++, --, +, -, ~, !
		
		var a = 10;
		
		a++;		// POST-INCREMENT
		System.out.println(a);
		
		++a;		// PRE-INCREMENT
		System.out.println(a);
		
		a--;		// POST-DECREMENT
		System.out.println(a);
		
		--a;		// PRE-DECREMENT
		System.out.println(a);
	}
}
