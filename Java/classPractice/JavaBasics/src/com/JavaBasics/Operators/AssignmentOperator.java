package com.JavaBasics.Operators;

public class AssignmentOperator {
	public static void main(String[] args) {
		
		// =, +=, -=, *=, /=, %=
		
		var a = 10;
		System.out.println(a);		// ASSIGN THE VALUE TO a
		
		a += 2;
		System.out.println(a);		// ADD AND ASSIGN
		
		a -= 4;
		System.out.println(a);		// SUBTRACT AND ASSIGN
		
		a *= 3;
		System.out.println(a);		// MULTIPLY AND ASSIGN
		
		a /= 12;
		System.out.println(a);		// DIVIDE AND ASSIGN
		
		a %= 3;
		System.out.println(a);		// MODULAS AND ASSIGN
	}
}
