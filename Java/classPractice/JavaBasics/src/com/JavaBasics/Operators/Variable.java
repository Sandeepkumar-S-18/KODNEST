package com.JavaBasics.Operators;

public class Variable {
	public static void main(String[] args) {
		var ageVar = 23;
		var nameVar = "Sandeep";
		var genderVar = 'M';
		var isMarriedVar = false;
		
		int ageDt = 23;
		String nameDt = "Sandeep";
		char genderDt = 'M';
		boolean isMarriedDt = false;
		
		System.out.println(ageVar);
		System.out.println(nameVar);
		System.out.println(genderVar);
		System.out.println(isMarriedVar);
		
		System.out.println(ageDt);
		System.out.println(nameDt);
		System.out.println(genderDt);
		System.out.println(isMarriedDt);
	}
}
